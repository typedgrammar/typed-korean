/**
 * Build-time reverse index: vocab word → tutorial example sentences that use
 * it. Reuses the SAME src/vocab/extract.ts the UI renders chips from, so the
 * reverse links correspond exactly to the word chips shown under each example.
 *
 * Emits src/tutorial/reverseIndex.generated.ts (committed, and regenerated on
 * every `pnpm build` / `pnpm dev`). Also validates the course ↔ vocab link and
 * prints a summary. It never fails the build on its own — verify-vocab.mjs is
 * the gate for that.
 *
 *   node scripts/gen-reverse-index.mjs
 */
import ts from "typescript";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.resolve(__dirname, "..", "src");

/** Transpile a single .ts module and run it in isolation (type imports are
 *  erased; runtime imports resolve to {}). The data modules here are
 *  self-contained — chapters use `import type` only, extract.ts is pure regex. */
function evalModule(file) {
  const js = ts.transpileModule(fs.readFileSync(file, "utf8"), {
    compilerOptions: {
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
    },
  }).outputText;
  const mod = { exports: {} };
  new Function("module", "exports", "require", js)(mod, mod.exports, () => ({}));
  return mod.exports;
}

// The exact extractor the UI uses, so links match the on-screen chips.
const { extractWords } = evalModule(path.join(SRC, "vocab/extract.ts"));

// Dictionary word sets — content words must be referenced; function words
// (particles, です…) aren't extracted, so they're expected to have no links.
const contentWords = new Set();
const entriesDir = path.join(SRC, "vocab/entries");
for (const f of fs.readdirSync(entriesDir).filter((f) => f.endsWith(".ts"))) {
  const mod = evalModule(path.join(entriesDir, f));
  for (const e of mod.default || mod.entries || []) if (e?.word) contentWords.add(e.word);
}
const functionWords = new Set();
for (const e of evalModule(path.join(SRC, "vocab/function-words.ts")).FUNCTION_WORDS || [])
  if (e?.word) functionWords.add(e.word);
const dict = new Set([...contentWords, ...functionWords]);

// Walk chapters → points → examples and invert the relationship.
const chaptersDir = path.join(SRC, "tutorial/chapters");
const chapterFiles = fs
  .readdirSync(chaptersDir)
  .filter((f) => f.endsWith(".ts") && f !== "index.ts")
  .sort();

const index = new Map(); // word → ExampleRef[]
const missing = new Map(); // word → count (extracted but absent from the dictionary)
let exampleCount = 0;

for (const f of chapterFiles) {
  const ch = evalModule(path.join(chaptersDir, f)).default;
  if (!ch?.id || !ch.points) continue;
  for (const pt of ch.points) {
    (pt.examples || []).forEach((ex, i) => {
      exampleCount++;
      const anchor = `ex-${ch.id}-${pt.id}-${i}`; // keep in sync with exampleAnchorId()
      const seen = new Set();
      for (const { word } of extractWords(ex.code)) {
        if (seen.has(word)) continue;
        seen.add(word);
        if (!dict.has(word)) {
          missing.set(word, (missing.get(word) || 0) + 1);
          continue;
        }
        if (!index.has(word)) index.set(word, []);
        index.get(word).push({
          chapterId: ch.id,
          chapterTitleEn: ch.titleEn,
          chapterTitleZh: ch.titleZh,
          pointTitleEn: pt.titleEn,
          pointTitleZh: pt.titleZh,
          anchor,
          jp: ex.jp,
        });
      }
    });
  }
}

// Emit, sorted by reading-ish (locale) for stable diffs.
const obj = {};
for (const w of [...index.keys()].sort((a, b) => a.localeCompare(b, "ja"))) obj[w] = index.get(w);

const out =
  `// AUTO-GENERATED by scripts/gen-reverse-index.mjs — DO NOT EDIT.\n` +
  `// Reverse index: vocab word → tutorial example sentences that reference it.\n` +
  `// Regenerated on every \`pnpm build\` / \`pnpm dev\`.\n\n` +
  `import type { ExampleRef } from "./types";\n\n` +
  `export const REVERSE_INDEX: Record<string, ExampleRef[]> = ${JSON.stringify(obj, null, 2)};\n`;
fs.writeFileSync(path.join(SRC, "tutorial/reverseIndex.generated.ts"), out);

// Summary / validation (informational).
const deadContent = [...contentWords].filter((w) => !index.has(w));
console.log(`reverse-index: ${exampleCount} examples · ${index.size} words linked`);
if (missing.size) {
  console.log(`  ⚠ ${missing.size} extracted word(s) not in the dictionary (no chip/link):`);
  for (const [w, n] of [...missing].sort((a, b) => b[1] - a[1]).slice(0, 20))
    console.log(`     ${w} ×${n}`);
}
console.log(
  `  ℹ ${deadContent.length}/${contentWords.size} content entries never referenced by an example`
);
